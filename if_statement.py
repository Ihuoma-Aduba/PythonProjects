I_want_to_eat = True
I_want_to_drink = False

#if
if I_want_to_eat:
    print("Let's have a pizza")
else: print("I'm good!")

#if or
if I_want_to_eat and I_want_to_drink:
    print("Let's have a pizza and drink")
else: print("I'm good!")

#if and
if I_want_to_eat or I_want_to_drink:
    print("Let's go to a restaurant")
else: print("I'm good!")

#el if
if I_want_to_eat:
    print("Let's go to a restaurant" )
elif I_want_to_drink:
    print("Let's have a drink")
else: print("I'm good!")

#if not
if not I_want_to_eat:
    print("It's all good")



