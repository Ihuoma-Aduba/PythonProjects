colors = ["blue", "red", "orange", "black"]
for one_color in colors:
    print(one_color)

for x in colors:
    print(x)

for letter in "blue":
    print(letter)

colors_2 = ["black", "yellow", "green", "red"]
for one_color_2 in colors_2:
    print(one_color_2)

#Break Statement
for one_color_2 in colors_2:
    print(one_color_2)
    if one_color_2 == "green":
        break

#Range Statement
for number in range(6):
    print(number)

for number in range(20, 30):
    print(number)